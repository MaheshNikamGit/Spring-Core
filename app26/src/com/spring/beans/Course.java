package com.spring.beans;

public class Course {
	public String cid;
	public String cname;
	public float ccost;
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public float getCcost() {
		return ccost;
	}
	public void setCcost(float ccost) {
		this.ccost = ccost;
	}
	
	
}
